package main;

/**
 * 計算処理
 * 
 * @author onesword0618
 */
public class Calculator {

	/**
	 * 加算処理
	 * 
	 * @param x
	 * @param y
	 * @return 加算結果
	 */
	public int pluss(int x, int y) {
		return x + y;
	}

	/**
	 * 減算処理
	 * 
	 * @param x
	 * @param y
	 * @return 減算結果
	 */
	public int minus(int x, int y) {
		return x - y;
	}

	/**
	 * 乗算処理
	 * 
	 * @param x
	 * @param y
	 * @return 乗算結果
	 */
	public int multiply(int x, int y) {
		return x * y;
	}

	/**
	 * 除算処理
	 * 
	 * @param x
	 * @param y
	 * @return 除算結果
	 */
	public int divided(int x, int y) {
		return x / y;
	}
}
